package org.brandao.brutos.paymentgateway.registry;

public class PaymentGatewayConfigRegistryException extends Exception{

	private static final long serialVersionUID = -4800481453724870910L;

	public PaymentGatewayConfigRegistryException() {
		super();
	}

	public PaymentGatewayConfigRegistryException(String message, Throwable cause) {
		super(message, cause);
	}

	public PaymentGatewayConfigRegistryException(String message) {
		super(message);
	}

	public PaymentGatewayConfigRegistryException(Throwable cause) {
		super(cause);
	}

}
